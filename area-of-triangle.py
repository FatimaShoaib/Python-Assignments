#12.	Write a Python program that will accept the base and height of a triangle and compute the area 
b = float(input('Enter base of a triangle(in cm): '))
h = float(input('Enter height of a triangle(in cm): '))
area = 0.5* b*h
print('Area of a triangle of base = ',b,' and height= ',h,'is\n',area,'sq.cm')